test("prompt-build hook observes and replaces systemPrompt", async () => {
  const observed: string[] = [];
  const hookNames: string[] = [];
  const debug: string[] = [];
  const hook = createPromptBuildHookBehavior({
    observe(value) {
      observed.push(value.developerInstructions);
      hookNames.push(value.hookName);
    },
    debug(message) {
      debug.push(message);
    },
  });

  const result = await hook(
    {
      systemPrompt: [
        STANDARD_CORE,
        "",
        "## OpenClaw Workspace Instructions",
        "",
        "Observed",
      ].join("\n"),
    },
    {},
  );

  assert.deepEqual(
    observed,
    [[STANDARD_CORE, "", "## OpenClaw Workspace Instructions", "", "Observed"].join("\n")],
  );
  assert.deepEqual(hookNames, ["before_prompt_build"]);
  assert.deepEqual(result, {
    systemPrompt: "## OpenClaw Workspace Instructions\n\nObserved",
  });
  assert.ok(debug.some((message) => message.includes("markerIndex=")));
  assert.ok(debug.some((message) => message.includes("return=systemPrompt")));
});

