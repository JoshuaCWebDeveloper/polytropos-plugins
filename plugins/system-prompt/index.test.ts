test("writes generated prompt verbatim and reconciles CODEX_HOME config", async () => {
  const root = await fs.mkdtemp(path.join(os.tmpdir(), "system-prompt-"));
  const promptPath = resolvePromptFilePath(root, { promptFile: "generated.md" });
  const cachePath = resolveInstructionsCacheFilePath(root);
  await fs.writeFile(path.join(root, "config.toml"), 'model = "gpt-5-codex"\n');
  const source = [
    "Follow OpenClaw instructions.",
    "",
    "## OpenClaw Workspace Instructions",
    "",
    "Workspace tail.",
  ].join("\n") + "\n";

  const result = await generatePromptAndReconcileConfig({
    codexHome: root,
    promptFilePath: promptPath,
    instructionsCacheFilePath: cachePath,
    developerInstructions: source,
  });

  assert.equal(result.promptChanged, true);
  assert.equal(result.configChanged, true);
  assert.equal(result.cacheChanged, true);
  assert.equal(await fs.readFile(promptPath, "utf8"), source);
  assert.equal(await fs.readFile(cachePath, "utf8"), source);
  assert.match(await fs.readFile(path.join(root, "config.toml"), "utf8"), /model_instructions_file/);
});

